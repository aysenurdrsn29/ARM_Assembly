//In this section I focused only on HEX0-3

.equ MPCORE_PRIV_TIMER,0xFFFEC600
.equ HEX3, 0xFF200020
.equ HEX4, 0xFF200030
.text
.global _start
_start:
LDR R0, =MPCORE_PRIV_TIMER
LDR R1, =HEX3
LDR R2, =HEX4
LDR R3, =200000000
STR R3, [R0]
MOV R4, #0x40
STR R4, [R1]
MOV R3, #0b011
STR R3, [R0, #8]
MOV R5, #0
LOOP:
LDR R3, [R0,#12]
CMP R3, #1
BNE LOOP
STR R3, [R0, #12]
CMP R5, #0
BEQ LEFT
BNE RIGHT
CONTROLR:
LDR R6, [R1]
CMP R6, #0x00000040
MOVEQ R5, #0
B LOOP
CONTROLL:
LDR R6, [R1]
CMP R6, #0x40000000
MOVEQ R5, #1
B LOOP
LEFT:
LSL R4, R4, #8
STR R4, [R1]
B CONTROLL
RIGHT:
LSR R4, R4, #8

STR R4, [R1]
B CONTROLR
.end